// Date helper: DD/MM/YYYY → YYYY-MM-DD
function d(s) {
  const [day, month, year] = s.split('/').map(Number);
  return `${year}-${String(month).padStart(2,'0')}-${String(day).padStart(2,'0')}`;
}

